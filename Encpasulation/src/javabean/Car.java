package javabean;

public class Car {
	private String brandString;
	private int cost;

	public void setBrandString(String brandString) {
		this.brandString = brandString;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}

	public String getBrandString() {
		return brandString;
	}
	public int getCost() {
		return cost;
	}

}
